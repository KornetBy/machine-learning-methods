"""
Реализуйте программу «Цветочный магазин», которая будет включать
в себя шесть пунктов меню. У вас есть словарь, где ключ – название
букета. Значение – список, который содержит состав букета(розы,
гвоздики,и т.п.), цену и кол-во (шт),которое есть в магазине.
1. Просмотр описания: название – описание
2. Просмотр цены: название – цена.
3. Просмотр количества: название – количество.
4. Всю информацию.
5. Покупка
В пункте «Покупка» необходимо совершить покупку, с
клавиатуры вводите название букета и его кол-во, n – выход из
программы. Посчитать цену выбранных товаров и сколько товаров
осталось в изначальном списке
6. До свидания
"""

shop = {
    'Розовый букет': ['розы, тюльпаны', 20, 10],
    'Летний букет': ['лилии, гвоздики', 15, 5],
    'Экзотический букет': ['орхидеи, герберы', 30, 3]
}

while True:
    print("\nМеню:")
    print("1. Просмотр описания")
    print("2. Просмотр цены")
    print("3. Просмотр количества")
    print("4. Вся информация")
    print("5. Покупка")
    print("6. До свидания")

    choice = input("Введите номер пункта меню: ")

    if choice == '1':
        print("\nОписание букетов:")
        for bouquet, details in shop.items():
            print(f"{bouquet} - Состав: {details[0]}")

    elif choice == '2':
        print("\nЦены на букеты:")
        for bouquet, details in shop.items():
            print(f"{bouquet} - Цена: {details[1]} рублей")

    elif choice == '3':
        print("\nКоличество букетов на складе:")
        for bouquet, details in shop.items():
            print(f"{bouquet} - Количество: {details[2]} шт")

    elif choice == '4':
        print("\nПолная информация о букетах:")
        for bouquet, details in shop.items():
            print(f"{bouquet} - Состав: {details[0]}, Цена: {details[1]} рублей, Количество: {details[2]} шт")

    elif choice == '5':
        bouquet_name = input("Введите название букета, который хотите купить (или 'n' для выхода): ")
        if bouquet_name.lower() == 'n':
            continue

        if bouquet_name in shop:
            quantity = int(input("Введите количество: "))
            if quantity <= shop[bouquet_name][2]:
                total_price = quantity * shop[bouquet_name][1]
                shop[bouquet_name][2] -= quantity
                print(f"Вы купили {quantity} шт '{bouquet_name}' на сумму {total_price} рублей.")
            else:
                print("Недостаточно товара на складе.")
        else:
            print("Букет не найден.")

    elif choice == '6':
        print("До свидания!")
        break

    else:
        print("Некорректный ввод. Пожалуйста, выберите пункт от 1 до 6.")

